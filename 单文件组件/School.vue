<template>
  <!-- 组件的结构 -->
  <div class="demo">
    <h1>学校名称：{{ schoolName }}</h1>
    <h1>学校地址：{{ schoolAddress }}</h1>
    <button @click="showName">点我提示信息</button>
  </div>
</template>


<script>
// 组件交互相关的代码（数据、方法等）
export default {
  name: "School",
  data() {
    return {
      schoolName: "尚硅谷",
      schoolAddress: "北京",
    };
  },
  methods: {
    showName() {
      alert(this.schoolName);
    },
  },
};
</script>

